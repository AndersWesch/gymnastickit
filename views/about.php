        <h2>About</h2>
        <p>Gymnastick It is a Stick It game for gymnasts and other flipping sports. Stick It is a simple game where each gymnast (player) will do the trick selected and the landing defines the score.</p>
        <p>Gymnastick It is a game completely on your own risk and you can modify the rules and equipment to your liking.</p>

        <h2>Scoring</h2>
        <p>The landing defines the score and there are three different options: </p>
        <p>Stick is a solid landing where no steps has been taken, it gives you 2 points.</p>
        <p>Step is a landing with a single step, it gives you 1 point.</p>
        <p>Nope is everything else and the score is 0 on Normal Mode and -1 on Hard Mode</p>

        <h2>Levels</h2>
        <p>There are 4 main levels which should be sufficient for almost any level of player.</p>
        <p>If you can't find the level you are looking for in the dropdown just type in the name.</p>
        <p>If you look in the <a style="color: #2f7dde" href="/levels">levels</a> tab you'll see the different skills in each level and maybe find a new level to try.</p>

        <h2>Adventure Mode</h2>
        <p>Adventure Mode is a special mode where the jumps build up little by little.</p>
        <p>There is no round limit but the game can be stopped whenever a new round begins.</p>
        <p>If you wish to start from another round just type in the round number and you will start later on in the build up.</p>
